class EmotionUil {
  String _emotion;
  String _emoji;

  EmotionUil(String emotion, String emoji) {
    _emotion = emotion;
    _emoji = emoji;
  }

  void setEmotion(String emotion) {
    _emotion = emotion;
  }

  String getEmotion() {
    return _emotion;
  }

  void setEmoji(String emoji) {
    _emoji = emoji;
  }

  String getEmoji() {
    return _emoji;
  }

}
